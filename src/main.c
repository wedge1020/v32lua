    printf("    POP   R4             ; Get ASCII char\n");
    printf("    MOV   [R5], R4       ; Write to string\n");
    printf("    IADD  R5, 1          ; Advance write head\n");
    printf("    ISUB  R3, 1          \n");
    printf("    JMP   __ftoa_write_loop\n");

    printf("\n__ftoa_finish:\n");
    printf("    MOV   R4, 0\n");
    printf("    MOV   [R5], R4       ; Null-terminate\n");
    printf("    OR    R0, 0x7FC00000 ; Box the new string pointer\n");
    printf("    JMP   __tostring_done\n");

