//
// v32lua: a Vircon32-targetting lua compiler, written in C. It compiles lua code to
//         Vircon32 assembly.
//
// NOTE: Vircon32 comparison instructions are destructive to destination register, 
//       and overwrite with a 0 (false) or 1 (true), which can then be used with
//       the JT or JF instructions. The comparisons are all 2 operand:
//
//       IEQ R0, R1 ; R0 = (R0 == R1)
//       JT  R0, _label
//
//       Integer comparison instructions are: IEQ, INE, ILT, ILE, IGT, and IGE
//       floating point comparisons are:      FEQ, FNE, FLT, FLE, FGT, and FGE
//
// There are 16 general purpose registers (R0-R15):
//
// R0  - R10: completely available for general use
// R11 - R13: general use, but are also used by MOVS, SETS, and CMPS
// R14 - R15: the stack base pointer and stack pointer register
//
////////////////////////////////////////////////////////////////////////////////////////

#include "v32lua.h"

extern ASTNode *root_node; // Ensure your bison grammar exports the root AST node

// Prototypes for functions generated by Flex/Bison
extern FILE *yyin;
extern int yydebug;

static void  print_version (const char *prog_name)
{
    fprintf (stdout, "%s %s\n", prog_name, VERSION);
    fprintf (stdout, "Lua Compiler for Vircon32 (v32lua) by %s\n", AUTHOR);
    fprintf (stdout, "  github: %s\n", URL);
}

static void  print_usage (const char *prog_name)
{
    fprintf (stdout, "Lua Compiler for Vircon32 (%s)\n", prog_name);
    fprintf (stdout, "USAGE: %s [options] file\n", prog_name);
    fprintf (stdout, "Options:\n");
    fprintf (stdout, "  --help       Displays this information\n");
    fprintf (stdout, "  --version    Displays compiler version\n");
    //fprintf (stdout, "  --debugmode  Creates files with results of internal stages\n");
    fprintf (stdout, "  -o <file>    Output file, default name is the same as input\n");
    //fprintf (stdout, "  -b           Compiles the program as a BIOS\n");
    fprintf (stdout, "  -v           Displays additional information (verbose)\n");
    fprintf (stdout, "  -O0          Disable compiler optimizations (default)\n");
    fprintf (stdout, "  -O1          Enable compiler optimizations\n");
    fprintf (stdout, "  -g           Outputs an additional file with debug info\n");
    //fprintf (stdout, "  -w           Inhibit all warnings\n");
    //fprintf (stdout, "  -Wall        Enable all warnings\n");
}

static void log_stage (int  stage_num, const char *stage_name, int  verbose)
{
    if (verbose)
    {
        fprintf (stdout, "stage %d: running %s\n", stage_num, stage_name);
    }
}

int  main (int  argc, char** argv)
{
    if (argc                   <  2)
    {
        print_usage (argv[0]);
        return (1);
    }

    char *input_filename        = NULL;
    char  output_filename[256]  = { 0 };
    int   verbose               = 0;

    // --- Command Line Argument Parsing ---
    for (int i = 1; i < argc; i++) {
        if (strcmp(argv[i], "-o") == 0 && i + 1 < argc) {
            strncpy(output_filename, argv[++i], sizeof(output_filename) - 1);
        } else if (strcmp(argv[i], "-g") == 0) {
            g_debug_mode = true;
        } else if (strcmp(argv[i], "--verbose") == 0 || strcmp(argv[i], "-v") == 0) {
            verbose = 1;
        } else if (strcmp(argv[i], "-O0") == 0) {
            o_optflag                      = 0;
        } else if (strcmp(argv[i], "-O1") == 0) {
            o_optflag                      = 1;
        } else if (strcmp(argv[i], "--version") == 0) {
            print_version(argv[0]);
            return 0;
        } else if (strcmp(argv[i], "--help") == 0 || strcmp(argv[i], "-h") == 0) {
            print_usage(argv[0]);
            return 0;
        } else if (input_filename == NULL) {
            input_filename = argv[i];
        } else {
            fprintf(stderr, "Compiler Error: Unknown or duplicate argument '%s'\n", argv[i]);
            return 1;
        }
    }

    if (input_filename == NULL) {
        fprintf(stderr, "Compiler Error: No input file specified.\n");
        return 1;
    }

    // Default output filename to input_filename.asm if not specified by -o
    if (output_filename[0] == '\0') {
        strncpy(output_filename, input_filename, sizeof(output_filename) - 5);
        char* last_dot = strrchr(output_filename, '.');
        if (last_dot != NULL) {
            strcpy(last_dot, ".asm");
        } else {
            strcat(output_filename, ".asm");
        }
    }

    g_lua_filename = input_filename;
    g_asm_filename = output_filename;

    // --- Stage 1: Lexer Setup ---
    log_stage(1, "lexer", verbose);
    yyin = fopen(input_filename, "r");
    if (yyin == NULL) {
        compiler_error(ERR_INTERNAL, -1, "Could not open input file '%s'", input_filename);
    }

    // --- Stage 2 & 3: Preprocessor & Parser ---
    log_stage(2, "preprocessor", verbose);
    log_stage(3, "parser", verbose);
    if (yyparse() != 0 || root_node == NULL) {
        fclose(yyin);
        compiler_error(ERR_SYNTAX, -1, "Parsing failed due to syntax errors.");
    }
    fclose(yyin);

    // --- Stage 4: Semantic Analyzer ---
    log_stage(4, "analyzer", verbose);
    init_global_scope();
    
    // Perform full symbol pre-pass before code generation
    register_all_globals_prepass(root_node);

    // --- AST OPTIMIZATION PASSES ---
	if (o_optflag            >= 1)
	{
		bool  ast_changed     = true;
		int   max_iterations  = 5; // Guard against infinite compiler loops
		int   iter            = 0;

		int   o_optimizations_performed  = 0; // this probably needs to be a global variable

		while (ast_changed && iter < max_iterations) {
			ast_changed = false;
        
			// You can add a global modification counter inside your optimize passes
			// e.g., g_optimizations_performed++ whenever a node mutates.
			int before_count = o_optimizations_performed;
        
			clear_const_table();
			root_node = propagate_constants(root_node);
			root_node = fold_constants(root_node);
        
			if (o_optimizations_performed > before_count) {
				ast_changed = true;
			}
			iter++;
		}
    
		if (g_debug_mode) {
			printf("[Optimizer] Converged after %d iteration(s).\n", iter);
		}
	}

    // --- Stage 5: Emitter ---
    log_stage(5, "emitter", verbose);

    // 1. Open the file specified by -o
    FILE *asm_file  = fopen (output_filename, "w");
    if (asm_file   == NULL)
    {
        compiler_error (ERR_INTERNAL, -1, "Could not open output file '%s' for writing", output_filename);
    }

    // 2. Point the out() infrastructure to this file
    set_output_stream (asm_file);

    // 3. Run the generator (it will now write to out())
    generate_program (root_node);

    // 4. Safely flush, close, and reset the stream infrastructure
    close_output_stream();

    // --- Artifact Notification ---
    if (verbose)
    {
        fprintf (stdout, "saving output file\n");
        fprintf (stdout, "saving cart config file\n");
    }
    
    emit_cart_xml (output_filename, verbose);

    if (verbose)
    {
        fprintf (stdout, "compilation successful\n");
    }

    return (0);
}

