//
// v32lua: a Vircon32-targetting lua compiler, written in C. It compiles lua code to
//         Vircon32 assembly.
//
// NOTE: Vircon32 comparison instructions are destructive to destination register, 
//       and overwrite with a 0 (false) or 1 (true), which can then be used with
//       the JT or JF instructions. The comparisons are all 2 operand:
//
//       IEQ R0, R1 ; R0 = (R0 == R1)
//       JT  R0, _label
//
//       Integer comparison instructions are: IEQ, INE, ILT, ILE, IGT, and IGE
//       floating point comparisons are:      FEQ, FNE, FLT, FLE, FGT, and FGE
//
// There are 16 general purpose registers (R0-R15):
//
// R0  - R10: completely available for general use
// R11 - R13: general use, but are also used by MOVS, SETS, and CMPS
// R14 - R15: the stack base pointer and stack pointer register
//
////////////////////////////////////////////////////////////////////////////////////////

#include <stdio.h>
#include <stdlib.h>
#include <stdarg.h>
#include <string.h>
#include "codegen.h"

// Prototypes for functions generated by Flex/Bison
extern int yyparse(void);
extern FILE* yyin;

// Declare the external symbol created by runtime_embed.S
extern const char runtime_asm_start[];

void  emit_runtime_library (void)
{
    emit_asm("\n;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;");
    emit_asm(";;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;\n");
    emit_asm(";;\n");
    emit_asm(";; Lua assembly support Runtime Library Subroutines\n");
    emit_asm(";;\n");
    emit_asm(";;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;");
    emit_asm(";;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;\n");
    emit_asm("\n");

    // Point our cursor to the start of the embedded Vircon32 assembly
    const char* cursor = runtime_asm_start;
    char line[1024];

    // Read until we hit the null terminator we appended in .S
    while (*cursor != '\0')
    {
        int i = 0;
        
        // Extract a single line up to the newline character or buffer capacity
        while (*cursor != '\0' && *cursor != '\n' && i < (int)sizeof(line) - 2)
        {
            line[i++] = *cursor++;
        }
        
        // Preserve the newline character if present
        if (*cursor == '\n')
        {
            line[i++] = *cursor++;
        }
        
        line[i] = '\0';
        
        // Pass the line through your special formatting function
        emit_asm("%s", line);
    }

    emit_asm("\n;;\n");
    emit_asm(";; End of Runtime Library\n");
    emit_asm(";;\n");
    emit_asm(";;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;");
    emit_asm(";;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;\n");
}

void compiler_error (ErrorType type, int line_num, const char* format, ...)
{
    // 1. Print the Error Type Prefix
    fprintf (stderr, "\n");
    switch (type)
    {
        case ERR_LEXICAL:
            fprintf (stderr, "[Lexical Error]");
            break;

        case ERR_SYNTAX:
            fprintf (stderr, "[Syntax Error]");
            break;

        case ERR_SEMANTIC:
            fprintf (stderr, "[Semantic Error]");
            break;

        case ERR_INTERNAL:
            fprintf (stderr, "[Internal Compiler Error]");
            break;

        default:
            fprintf (stderr, "[Unknown Error]");
            break;
    }
    
    if (line_num >  0)
    {
        fprintf (stderr, " on line %d: ", line_num);
    }
    else
    {
        fprintf (stderr, ": ");
    }

    // 2. Format and print the custom message
    va_list args;
    va_start (args, format);
    vfprintf (stderr, format, args);
    va_end (args);
    fprintf (stderr, "\n");

    // 3. Highlight the line of code (if we have a valid line number and file)
    if (line_num > 0 && yyin != NULL)
    {
        rewind (yyin);
        char  line_buffer[1024];
        int   current_line    = 1;
        while (fgets(line_buffer, sizeof(line_buffer), yyin))
        {
            if (current_line == line_num)
            {
                fprintf (stderr, "      |\n");
                fprintf (stderr, " %4d | %s", line_num, line_buffer);
                if (strchr(line_buffer, '\n') == NULL)
                {
                    fprintf (stderr, "\n");
                }
                fprintf (stderr, "      |\n\n");
                break;
            }
            current_line++;
        }
    }

    // 4. Halt compilation
    exit (1);
}

extern int yydebug;

int main (int  argc, char** argv)
{
    if (argc > 1) {
        FILE* file = fopen(argv[1], "r");
        if (!file) {
            perror("Error opening source file");
            return 1;
        }
        yyin = file;
    } else {
        printf("; Reading from standard input (type your code and press Ctrl+D):\n");
    }

    #if YYDEBUG
    yydebug = 0;
    #endif

    // Run the Parser (which loops calls to the Code Generator automatically)
    if (yyparse() != 0) {
        fprintf(stderr, "Compiler Aborted: Parsing error encountered.\n");
        return 1;
    }

    return (0);
}
